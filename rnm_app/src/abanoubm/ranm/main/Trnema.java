package abanoubm.ranm.main;

public class Trnema {

	private String title;
	private String body;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBody() {
		return body;
	}
	public Trnema(String title, String body) {
		super();
		this.title = title;
		this.body = body;
	}
	public void setBody(String body) {
		this.body = body;
	}


}
